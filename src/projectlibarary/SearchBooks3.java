    private void loadBookDetails(){
        String query = "SELECT * FROM [Books Data] WHERE Title = ?";
        try(PreparedStatement stmt = con.prepareStatement(query);){ 
            stmt.setString(1, BookName);
            try(ResultSet rs = stmt.executeQuery()){
                if(rs.next()){
                   jTextField1_name.setText(rs.getString("Title"));
                   jTextField2_author.setText(rs.getString("Author"));
                   jTextArea1.setText(rs.getString("Description"));
                   jLabel7CoverTitle.setText(rs.getString("Title"));//add title cover
                   BookId = rs.getInt("BookID");
                   java.sql.Date pubDate = rs.getDate("PublicationDate");
                   if(pubDate != null){
                       String formatDate = new SimpleDateFormat("yyy-MM-dd").format(pubDate);
                       jTextField3_pubDate.setText(formatDate);
                   }
                   //make the description in multi line
                   jTextArea1.setLineWrap(true);//ينقز سطر اذا وصل لنهاية التكست
                   jTextArea1.setWrapStyleWord(true);//يتأكد ان الكلمات ما تنقطع اذا نقز سطر
                   
                   jTextField1_name.setEditable(false);
                   jTextField2_author.setEditable(false);
                   jTextField3_pubDate.setEditable(false);
                   jTextArea1.setEditable(false);
                   //book state 
                   String state = rs.getString("Availability");
                   jLabel6_state.setText(state);
                   jLabel6_state.setForeground("Available".equalsIgnoreCase(state) ? Color.GREEN : Color.RED);
                   if(state.equals("Checked Out")){
                        jButton2_borrow.setEnabled(false);
                   }
                }
            }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(this, "Error loading book details "+ex.getMessage());
        }
    }

