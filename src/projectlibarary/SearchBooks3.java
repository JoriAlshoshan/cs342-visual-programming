private boolean checkOverdueBooks(int userId) {
    String query = "SELECT * FROM [Borrowing Data] WHERE UserID = ?";
    try (PreparedStatement stmt = con.prepareStatement(query)) {
        stmt.setInt(1, userId);
        try (ResultSet rs = stmt.executeQuery()) {
//            if(!rs.next()){
//                System.out.println("No overdue books found!");
//            }
            if (rs.next()) {
                String status = rs.getString("ReturnStatus");
                String status_2 = rs.getString("ReturnDate");
                
                if(status.equals("Late")&& status_2.equals("")){
                JOptionPane.showMessageDialog(this, "You have overdue books.\nBorrowing is not allowed.", "Overdue Books", JOptionPane.WARNING_MESSAGE);
                jButton2_borrow.setEnabled(false);
                return true;
                }
            }
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error checking overdue books: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    return false;
}
